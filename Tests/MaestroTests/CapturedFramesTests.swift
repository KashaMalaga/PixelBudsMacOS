            0x37, 0x35, 0x12, 0x13, 0x72, 0x65, 0x6c, 0x65, 0x61, 0x73, 0x65, 0x5f, 0x35, 0x2e, 0x31, 0x31,
            0x5f, 0x73, 0x69, 0x67, 0x6e, 0x65, 0x64, 0x29, 0x28, 0x4b, 0x58, 0xf5, 0x03, 0x63, 0x52, 0x0e,
            0x30, 0x00, 0x08, 0x01, 0x10, 0x13, 0x1d, 0xea, 0x71, 0xde, 0x7d, 0x5e, 0x25, 0x44, 0xfa, 0x99,
            0x71, 0x38, 0xff, 0xff, 0xff, 0xff, 0x0f, 0x42, 0xc5, 0x69, 0x4e, 0x7e,
        ]

        let decoder = HDLCDecoder()
        var frames: [HDLCFrame] = []
        var errors: [HDLCDecoder.DecodeError] = []
        decoder.process(Data(raw), onFrame: { frames.append($0) }, onError: { errors.append($0) })

        XCTAssertEqual(errors, [], "Captured frame should decode without errors")
        let frame = try XCTUnwrap(frames.first, "Expected exactly one decoded frame")
        XCTAssertEqual(frames.count, 1)

        XCTAssertEqual(frame.control, HDLCConsts.unnumberedControl, "Maestro frames use U-frame control byte 0x03")
        XCTAssertGreaterThan(frame.data.count, 0)

        let firmware = Data("release_5.11_signed".utf8)
        let build = Data("575".utf8)
        XCTAssertNotNil(frame.data.range(of: firmware), "Decoded payload should contain firmware string")
        XCTAssertNotNil(frame.data.range(of: build), "Decoded payload should contain build code 575")
    }

    /// Frame 5 contains an escaped 0x7e mid-payload (0x7d 0x5e at offset 0x13).
    /// Decoding should unescape it to a literal 0x7e in the data.
    func testDecodeFrameWithEscapedByte() throws {
        let raw: [UInt8] = [
            0x7e, 0x00, 0xa5, 0x03, 0x2a, 0x04, 0x22, 0x02, 0x68, 0x03, 0x08, 0x07, 0x10, 0x15, 0x1d, 0xea,
            0x71, 0xde, 0x7d, 0x5e, 0x25, 0xf5, 0xad, 0x21, 0x28, 0x38, 0x09, 0x08, 0xe4, 0x5a, 0xd2, 0x7e,
        ]

        let decoder = HDLCDecoder()
        var frames: [HDLCFrame] = []
        var errors: [HDLCDecoder.DecodeError] = []
        decoder.process(Data(raw), onFrame: { frames.append($0) }, onError: { errors.append($0) })

        XCTAssertEqual(errors, [])
        let frame = try XCTUnwrap(frames.first)
        XCTAssertEqual(frames.count, 1)
        XCTAssertTrue(frame.data.contains(0x7e), "Escaped 0x7e should appear unescaped in decoded data")
    }

